#include <stdio.h>
/*
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/*
 * sub - minus two integers
 * @a: first integer
 * @b: second integer
 * Return: give subtraction result
 */

int sub(int a, int b)
{
	return (a - b);
}

/*
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: division result
 */

int div(int a, int b)
{
	return (a / b);
}

/*
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: answer to the multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/*
 * mod -find mod of two integers
 * @a: first integer
 * @b: second integer
 * Return: mod of two numbers
 */

int mod(int a, int b)
{
	return (a % b);
}


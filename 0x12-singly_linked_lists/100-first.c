#include <stdio.h>

/**
 * bmain - Function executed before main.
 *
 * Description: This function is marked with the attribute constructor
 * to specify that it should be executed before main. It prints a message
 * to the standard output when the program is executed.
 *
 * Return: No return value.
 */
void __attribute__((constructor)) bmain(void)
{
	/* Print a message to the standard output */
	printf("You're beat! and yet, you must allow,\n");
	printf("I bore my house upon my back!\n");
}

